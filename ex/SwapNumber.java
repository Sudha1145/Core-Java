class SwapNumber
{
	public static void main(String[] args)
	{
		int a=20;
		int b=15;
		System.out.println("Numbers Before Swapping");
		System.out.println("First number: " + a);
        System.out.println("Second number: " + b);
		int temp=a;
		a=b;
		b=temp;
		System.out.println("Number After Swapping");
		System.out.println("First number: " + a);
        System.out.println("Second number: " + b);
	}
}
		