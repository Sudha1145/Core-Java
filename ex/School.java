class School
{
	public static void main(String[] args)
	{
		char grade = 'A';// A single letter grade
		System.out.println("grade:"+grade);
		
		char gender = 'F';// A single character representing gender
		System.out.println("gender:"+gender);
		
		char direction = 'N';// cardinal directions (e.g., 'N' for North)
		System.out.println("direction:"+direction);
		
		char initial = 'J';// The initial of person's last name
		System.out.println("initial:"+initial);
		
		char symbol = '@';//A single special character
		System.out.println("symbol:"+symbol);
		
		char response = 'Y';// A single character response
		System.out.println("response:"+response);
		
		char bloodType = 'O';//A single character for bloodType
		System.out.println("bloodType:"+bloodType);
		
		char chessPiece = 'Q';//initial of a chess piece
		System.out.println("chessPiece:"+chessPiece);
		
		char operation = '+';// A mathematical operation
		System.out.println("operation:"+operation);
		
		char trafficSignal = 'G';// A single character for traffic signal
		System.out.println("trafficSignal:"+trafficSignal);
		
	}
}